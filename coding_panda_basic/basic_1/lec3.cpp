#include <iostream> // 전처리 지시자

/*
c++에서 그 함수를 사용하고자 한다면?
반드시 함수의 원형을 미리 정의해야 한다.
*/

using namespace std;
// std : 문자열 같은 느낌
// ' ; ' : 종결자, 줄을 바꿀 때 사용, 한 문장 끝날 때 사용해야함

// f5 하면 출력
int main() {
	// c++ 코드에는 반드시 main이란 함수가 있어야 한다.
	cout << "Hello, World" << endl;
	// cout == print 느낌
	
	// ' << ' : 데이터의 흐름을 의미

	return 0;
}