#include <iostream>

using namespace std;

int main() {
	// char : 작은 문자열

	int a = 77;
	char b = 'a';

	char c[] = { 'a', 'b', 'c', '\0'};

	cout << a << endl;
	cout << b << endl;
	cout << c << endl;

	// bool : 0 혹은 1

	bool d = 0;
	bool e = 1;
	bool f = 10;

	cout << d << " " << e << " " << f << endl;

	return 0;

}
